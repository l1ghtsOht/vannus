def get_all_categories():
    """Return a sorted list of unique categories from the tool list."""
    cats = set()
    for t in TOOLS:
        for c in t.categories:
            cats.add(c.lower())
    return sorted(cats)


def get_all_tools_dict() -> list:
    """Return all tools as a list of dicts (for API / serialization)."""
    return [t.to_dict() for t in TOOLS]


